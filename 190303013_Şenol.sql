USE Pokemon
CREATE TABLE POKEMON(
	PNAME VARCHAR(50),
	ELEMENT VARCHAR(50),
	POKEDEXN INTEGER NOT NULL,
	MAXLEVEL INTEGER,
	SPECIES VARCHAR(50),
	TRAINERIDN INTEGER NOT NULL,
	PRIMARY KEY (POKEDEXN),
	)
CREATE TABLE GYM(
	GADDRES VARCHAR(50),
	LEADERNAME VARCHAR(50),
	GYMNAME VARCHAR(50) NOT NULL,
	PRIMARY KEY (GYMNAME),
	);
CREATE TABLE FACILITY(
	FACILITYID INTEGER NOT NULL,
	FADDRES VARCHAR(50),
	FNAME VARCHAR(50),
	PRIMARY KEY (FACILITYID),
	);
CREATE TABLE ITEMS(
	ITEMID INTEGER NOT NULL,
	ITEMNAME VARCHAR(50),
	PRICE INTEGER,
	PRIMARY KEY (ITEMID),
	);
CREATE TABLE HUMAN(
	HNAME VARCHAR(50),
	MAIL VARCHAR(50),
	PNUMBER INTEGER NOT NULL,
	JOB VARCHAR(50),
	PRIMARY KEY (PNUMBER),
	);
CREATE TABLE TRAINER(
	TRAINERIDT INTEGER NOT NULL,
	HNAMET VARCHAR(50),
	MAILT VARCHAR(50),
	JOBT VARCHAR(50),
	PNUMBERT INTEGER NOT NULL,
	PRIMARY KEY (TRAINERIDT),
	);
CREATE TABLE GYMMASTER(
	GYMIDNG INTEGER NOT NULL,
	HNAMEG VARCHAR(50),
	MAILG VARCHAR(50),
	JOBG VARCHAR(50),
	PNUMBERG INTEGER NOT NULL,
	GYMNAMES VARCHAR(50),
	PRIMARY KEY (GYMIDNG),
	);
CREATE TABLE CASUAL(
	CASUALID INTEGER NOT NULL,
	HNAMEC VARCHAR(50),
	JOBC VARCHAR(50),
	MAILC VARCHAR(50),
	PNUMBERC INTEGER NOT NULL,
	PRIMARY KEY (CASUALID),
	);
CREATE TABLE WORK(
	CASUALIDN INTEGER NOT NULL,
	FACILITYN INTEGER NOT NULL,
	FADDRESN VARCHAR(50) NOT NULL,
	FFNAME VARCHAR(50),
	WORKERNAME VARCHAR(50),
	PRIMARY KEY(FACILITYN,CASUALIDN),
	);
CREATE TABLE HOLDS(
	ITEMIDN INTEGER NOT NULL,
	TRAINERIDIN INTEGER NOT NULL,
	PRIMARY KEY (ITEMIDN,TRAINERIDIN),

	);
